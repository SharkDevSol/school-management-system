const AppContext = createContext();

// Alkhwarizm Brand Color System
const DEFAULT_THEME = {
  primaryColor: '#6F56FF',    // Digital Violet - Main identity color
  secondaryColor: '#8FA2FF',  // Soft Indigo - Hover states, secondary buttons
  mode: 'light'
};

export const AppProvider = ({ children }) => {
  const [theme, setTheme] = useState(DEFAULT_THEME);
  
  const [language, setLanguage] = useState('en');
  
  const [profile, setProfile] = useState({
    name: 'Administrator',
    email: '',
    profileImage: null
  });

  const [websiteName, setWebsiteName] = useState('School Management System');

  // Load saved settings on mount - prioritize database over localStorage
  useEffect(() => {
    const loadSettings = async () => {
      // First load from localStorage for immediate display
      const savedTheme = localStorage.getItem('appTheme');
      const savedLanguage = localStorage.getItem('appLanguage');
      const savedProfile = localStorage.getItem('adminUser');
      const savedProfileImage = localStorage.getItem('profileImage');

      if (savedTheme) {
        const parsedTheme = JSON.parse(savedTheme);
        setTheme(parsedTheme);
        applyTheme(parsedTheme);
      }
      
      if (savedLanguage) {
        setLanguage(savedLanguage);
        applyLanguageDirection(savedLanguage);
      }
      
      if (savedProfile) {
        const user = JSON.parse(savedProfile);
        setProfile(prev => ({
          ...prev,
          name: user.name || 'Administrator',
          email: user.email || ''
        }));
      }
      
      if (savedProfileImage) {
        setProfile(prev => ({ ...prev, profileImage: savedProfileImage }));
      }

      // Then load branding from database (this overrides localStorage)
      try {
        const response = await axios.get('https://excellence.oddag.et/api/admin/branding');
        const data = response.data;
        
        // Update website name from database
        if (data.website_name) {
          setWebsiteName(data.website_name);
          document.title = data.website_name;
        }
        
        // Update theme from database
        if (data.primary_color || data.secondary_color) {
          const dbTheme = {
            primaryColor: data.primary_color || DEFAULT_THEME.primaryColor,
            secondaryColor: data.secondary_color || DEFAULT_THEME.secondaryColor,
            mode: data.theme_mode || 'light'
          };
          setTheme(dbTheme);
          applyTheme(dbTheme);
          localStorage.setItem('appTheme', JSON.stringify(dbTheme));
        }
        
        // Update favicon from database
        if (data.website_icon) {
          const iconUrl = `https://excellence.oddag.et/uploads/branding/${data.website_icon}`;
          const link = document.querySelector("link[rel*='icon']") || document.createElement('link');
          link.type = 'image/x-icon';
          link.rel = 'shortcut icon';
          link.href = iconUrl;
          document.getElementsByTagName('head')[0].appendChild(link);
        }
      } catch (error) {
        console.log('Could not load branding from database, using localStorage');
        // Fallback to localStorage for website name
        const savedWebsiteName = localStorage.getItem('websiteName');
        if (savedWebsiteName) {
          setWebsiteName(savedWebsiteName);
          document.title = savedWebsiteName;
        }
      }
    };
    
    loadSettings();
  }, []);

  const applyTheme = (themeData) => {
    // Set primary and secondary colors
    document.documentElement.style.setProperty('--primary-color', themeData.primaryColor);
    document.documentElement.style.setProperty('--secondary-color', themeData.secondaryColor);
    
    // Calculate and set computed color variables based on primary color
    const primaryRgb = hexToRgb(themeData.primaryColor);
    if (primaryRgb) {
      const { r, g, b } = primaryRgb;
      document.documentElement.style.setProperty('--primary-light', `rgba(${r}, ${g}, ${b}, 0.1)`);
      document.documentElement.style.setProperty('--primary-lighter', `rgba(${r}, ${g}, ${b}, 0.05)`);
      document.documentElement.style.setProperty('--primary-shadow', `rgba(${r}, ${g}, ${b}, 0.3)`);
      document.documentElement.style.setProperty('--primary-shadow-lg', `rgba(${r}, ${g}, ${b}, 0.35)`);
    }
    
    // Apply dark mode class
    if (themeData.mode === 'dark') {
      document.body.classList.add('dark-mode');
    } else {
      document.body.classList.remove('dark-mode');
    }
  };

  const applyLanguageDirection = (lang) => {
    if (lang === 'ar') {
      document.documentElement.dir = 'rtl';
      document.body.classList.add('rtl');
    } else {
      document.documentElement.dir = 'ltr';
      document.body.classList.remove('rtl');
    }
  };

  const updateTheme = (newTheme) => {
    setTheme(newTheme);
    applyTheme(newTheme);
    localStorage.setItem('appTheme', JSON.stringify(newTheme));
  };

  const updateLanguage = (lang) => {
    setLanguage(lang);
    applyLanguageDirection(lang);
    localStorage.setItem('appLanguage', lang);
  };

  const updateProfile = (newProfile) => {
    setProfile(prev => ({ ...prev, ...newProfile }));
    
    // Update adminUser in localStorage
    const adminUser = JSON.parse(localStorage.getItem('adminUser') || '{}');
    adminUser.name = newProfile.name || profile.name;
    adminUser.email = newProfile.email || profile.email;
    localStorage.setItem('adminUser', JSON.stringify(adminUser));
    
    if (newProfile.profileImage) {
      localStorage.setItem('profileImage', newProfile.profileImage);
    }
  };

  const t = (key) => {
    return translations[language]?.[key] || translations['en'][key] || key;
  };

  const updateWebsiteName = (name) => {
    setWebsiteName(name);
    localStorage.setItem('websiteName', name);
    document.title = name;
  };

  return (
    <AppContext.Provider value={{
      theme,
      updateTheme,
      language,
      updateLanguage,
      profile,
      updateProfile,
      websiteName,
      updateWebsiteName,
      t,
      translations
    }}>
      {children}
    </AppContext.Provider>
  );
};

export const useApp = () => {
  const context = useContext(AppContext);
  if (!context) {
    throw new Error('useApp must be used within an AppProvider');
  }
  return context;
};

export default AppContext;
